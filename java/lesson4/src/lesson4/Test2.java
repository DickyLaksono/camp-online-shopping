package lesson4;

public class Test2 {
	
	public void doIt(){
		int num = 10;
		//perhitungannya akan berlanjut dan berurutan ,perhitungannya tidak mulai dari paling atas lagi 
		System.out.println(num);
		
		num = num + 1;
				
		System.out.println(num);
		
		num += 1;
		
		System.out.println(num);
		
		num ++;//ini yang sering di pakai, ini count up
		
		System.out.println(num);
		
		num --;//ini count down
		
		System.out.println(num);
	}
}
